    print(f"Текущее количество записей: {collection.count()}")

    batch_size = 50
    added = 0
    for i in range(0, len(CONSTITUTION), batch_size):
        batch = CONSTITUTION[i:i+batch_size]
        ids = [f"const_uz_{i+j}" for j in range(len(batch))]

        # Проверяем существующие ID
        try:
            existing = collection.get(ids=ids)
            existing_ids = set(existing['ids'])
        except:
            existing_ids = set()

        new_docs = [(doc, id_) for doc, id_ in zip(batch, ids) if id_ not in existing_ids]
        if new_docs:
            docs, new_ids = zip(*new_docs)
            collection.add(documents=list(docs), ids=list(new_ids))
            added += len(new_docs)
            print(f"  Добавлено {len(new_docs)} статей (батч {i//batch_size + 1})")

    print(f"\n✅ Конституция загружена! Добавлено: {added} статей")
    print(f"Итого в базе: {collection.count()} записей")

